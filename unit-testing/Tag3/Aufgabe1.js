
// Unit-Tests werden genutzt, um einzelne Methoden oder Funktionen einer
// Softwareanwendung zu testen. Sie sind schnell, effizient und helfen,
// Fehler frühzeitig zu erkennen.

// Integrationstests überprüfen, wie verschiedene Teile einer Anwendung 
// miteinander interagieren. Sie werden genutzt, um Fehler bei der 
// Zusammenarbeit zwischen Komponenten zu finden.

// End-to-End-Tests sind quasi das Vollprogramm. Sie simulieren die 
// Benutzerinteraktion mit der Anwendung und dienen dazu, Probleme zu 
// finden, die nur im Zusammenspiel aller Komponenten auftreten.